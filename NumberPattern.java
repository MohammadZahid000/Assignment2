package Assignment2;
public class NumberPattern {
	    public static void main(String[] args) {
	        int rows = 5; // Number of rows
	        int columns = 5; // Number of columns

	        for (int i = 1; i <= rows; i++) {
	            for (int j = 1; j <= columns; j++) {
	                System.out.print(i + "  "); 
	            }
	            System.out.println(); 
	        }
	    }

}

	